public class Swap_of_two_no {
    public static void main(String[] args) {
        int num1=2;
        int num2=3;
        System.out.println("the vale of num 1 before swap is :" +num1);
        System.out.println("the vale of num 1 before swap is :" +num2);
        int temp; // temp variable is used to  swap a two no's
         temp=num1;
         num1=num2;
         num2=temp;
        System.out.println("The value of num 1 after swap is :"+num1);
        System.out.println("the value of num 2 after swap is :"+num2);

    }
}
